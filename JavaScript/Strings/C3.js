//charAt()
userName = "NameOne"
console.log(userName.charAt(0)) //N
console.log(userName.charAt(2)) //M
console.log(userName.charAt(12))

//charCodeAt()
product = "Apple"
console.log(product.charCodeAt('0')) // 65
console.log(product.charCodeAt('4')) // 101

//includes
userName = "NameOne"
console.log(userName.includes('N')) //true
console.log(userName.includes('f')) //false

//indexOf()
userName = "K Sai Kiran"
console.log(userName.indexOf('K'))
console.log(userName.indexOf('K', 3))

//lastIndexOf()
userName = "KSai Kumar"
console.log(userName.lastIndexOf('K'))
console.log(userName.lastIndexOf('K', 3))
